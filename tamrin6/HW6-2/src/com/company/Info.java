package com.company;

public class Info {
    public String name;
    public String family;
    public int age;
    public String email;


    public String getName() {
        return name;
    }
    public Info setName(String name){
        this.name=name;
        return this ;
    }
    public String getFamily() {
        return name;
    }
    public Info setFamily(String family){
        this.family=family;
        return this ;
    }
    public int getAge() {
        return age;
    }
    public Info setAge(int age){
        this.age=age;
        return this ;
    }
    public String getEmail() {
        return email;
    }
    public Info setEmail(String email){
        this.email=email;
        return this ;
    }
}
